package com.example.aplikasibdm;

public class Bangun {
	private String nama;
	private int gambar;
	private String materi;
	private String ciri;
	private Rumus rumus;
	private Soal soal;
	public String getNama() {
		return nama;
	}
	public void setNama(String nama) {
		this.nama = nama;
	}
	public int getGambar() {
		return gambar;
	}
	public void setGambar(int gambar) {
		this.gambar = gambar;
	}
	public String getMateri() {
		return materi;
	}
	public void setMateri(String materi) {
		this.materi = materi;
	}
	public String getCiri() {
		return ciri;
	}
	public void setCiri(String ciri) {
		this.ciri = ciri;
	}
	public Rumus getRumus() {
		return rumus;
	}
	public void setRumus(Rumus rumus) {
		this.rumus = rumus;
	}
	public Soal getSoal() {
		return soal;
	}
	public void setSoal(Soal soal) {
		this.soal = soal;
	}
	
	
}
